Untuk meningkatkan pelayanan bagi pelanggan setia Suzuki, PT. Suzuki Indomobil Sales mempersembahkan program terbaru yaitu “Suzuki Express Maintenance“. Dengan pelayanan yang bermoto “<b>Time is yours</b>“, pelangggan Suzuki dapat melakukan perbaikan kendaraannya dengan waktu yang lebih cepat dibandingkan dengan perbaikan regular. Dengan adanya program Suzuki Express Maintenance pelanggan setia Suzuki tidak perlu menunggu lama dalam proses perbaikan kendarannya. <br />
<br />
Mungkin para konsumen akan melontarkan pertanyaan, Mengapa dengan fasilitas Express Maintenance (EM) pekerjaan servis dapat diselesaikan lebih cepat?<br />
Pekerjaan servis berkala dengan Express Maintenance (EM) dapat dilakukan 2-3 kali lebih cepat dari servis normal pada umumnya hal tersebut dikarenakan pekerjaan servis dilakukan oleh 2 orang mekanik handal yang telah terpilih dan bersertifikasi dibawah pelatihan oleh PT. Suzuki Indomobil Sales, serta 1 orang inspector yang bertugas menginspeksi setiap pekerjaan mekanik. Pekerjaan menjadi lebih cepat didukung pula oleh peralatan khusus EM yang mampu memudahkan proses kerja servis.<br />
<br />
Lalu bagaimana dengan kualitas servis yang didapatkan konsumen?<br />
Konsumen tidak perlu khawatir dan ragu terhadap kualitas dari pekerjaan servis dengan fasilitas EM. Karena, pelayanan Express Maintenance memegang komitmen dimana konsumen akan mendapatkan hasil kualitas dan biaya yang sama seperti halnya servis normal tanpa adanya penambahan biaya pelayanan. Sehingga, menjadi sebuah tambahan keuntungan bagi konsumen dengan fasilitas Express Maintenance ini.<br />
